    PASSWORD VARBINARY(MAX) NOT NULL,
    CONSTRAINT FK_KEYS_USUARIOS FOREIGN KEY (ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO) ON DELETE CASCADE
