    ID_ROL INT NOT NULL,
    CONSTRAINT FK_USUARIOS_ROLES FOREIGN KEY (ID_ROL) REFERENCES ROLES(ID_ROL)
);

CREATE TABLE KEYS_USUARIOS ( 
    ID_USUARIO INT PRIMARY KEY,
