-- PROCEDURES

CREATE PROCEDURE SP_NEXT_ID (@tabla NVARCHAR(50), @siguienteId INT OUTPUT)
AS
    BEGIN
        DECLARE @columna NVARCHAR(60);
        DECLARE @query NVARCHAR(MAX);
        DECLARE @longitud INT = LEN(@tabla);

        IF UPPER(@tabla) = 'MENSAJES'
            SET @columna = 'ID_MENSAJE';
        ELSE IF UPPER(RIGHT(@tabla, 2)) = 'ES'
            SET @columna = 'ID_' + UPPER(LEFT(@tabla, @longitud - 2));
        ELSE
            SET @columna = 'ID_' + UPPER(LEFT(@tabla, @longitud - 1));

        SET @query = N'SELECT @idOut = ISNULL(MAX(' + QUOTENAME(@columna) + '), 0) + 1 FROM ' + QUOTENAME(@tabla);

        EXEC sp_executesql @query, N'@idOut INT OUTPUT', @idOut = @siguienteId OUTPUT;
    END
GO